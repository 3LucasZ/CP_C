const int tcs = 1;

template<class T> 
T v_rand(vector<T> v){
    T i = rand() % sz(v);
    return v[i];
}

void tree(int l, int r) {
    cout << r-l+1 << nl;
    vector<int> pars = {l};
    for (int u=l+1;u<=r;u++){
        int v = v_rand(pars);
        cout << u << " " << v << nl;
        pars.push_back(u);
    }
}

void gen(){
    tree(0,10);
}

int main() {
    srand(time(0));
    if (tcs) {
        cout << tcs << nl;
        for (int i=0;i<tcs;i++) gen();
    }
    else {
        gen();
    }
    return 0;
}