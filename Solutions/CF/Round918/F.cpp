const bool multi = true;













void solve(){
    int n; cin >> n;
    vector<pair<int,int>> p; //points in order
    for (int i=0;i<n;i++){
        int x,y; cin >> x >> y;
        p.push_back({x,y});
        p.push_back({y,x});
    }
    sort(all(p));
    dbg(n,p);

    ordered_set<int> run;
    ll ans = 0;
    for (int i=0;i<2*n;i++){
        if (run.find(p[i].second) != run.end()){
            ans += run.order_of_key(p[i].second);
            run.erase(p[i].second);
        } else {
            run.insert(p[i].first);
        }
    }

    cout << ans << nl;
}

